			<b>How do Faults Produce Quakes?</b><br><br>

			In the following activity, you will learn how earthquakes are related to faults.<br><br>


			

			Imagine the boxes as the ground, and the boundary between them as a fault. Energy from inside the Earth makes the ground move. You simulate this by pulling on the rubber band. There is no movement right away because of friction. (What represents friction in the activity?)<br><br>

			Once friction is overcome, the ground suddenly moves and an earthquake occurs. Some scientists describe this process as stick and slip. At first, the rocks are stuck together due to friction. Later, the rocks suddenly slip, generating an earthquake. Every time a fault slips, the Earth quakes.<br><br>

