			<b>What is a Fault?</b><br><br>

			On July 16, 1990, a strong earthquake hit Luzon. Have you heard about it? Maybe not, because it happened before you were born. But if your parents are from Luzon, they surely know about the earthquake. It is possible that they were even affected by it.<br><br>

			During that earthquake, many people lost their lives and many more were injured. A lot of buildings and other structures were either damaged or destroyed. The earthquake had a magnitude of 7.8 and its epicenter was located in Nueva Ecija. According to scientists, the earthquake was caused by movement along the Philippine Fault.<br><br>

			Fault, epicenter, magnitude. Do you know what these terms mean? You will learn about them in this module. Let us start with faults.<br><br>

			Earthquakes are associated with faults. When a fault suddenly moves, an earthquake occurs. Do you know what a fault is? Do the following activity to find out.<br><br>

			
